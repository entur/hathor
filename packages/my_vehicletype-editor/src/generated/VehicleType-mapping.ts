/*
 * Project VehicleType to fast-xml-parser XMLBuilder shape.
 * Renames $-prefixed attrs to @_, stringifies booleans,
 * and delegates complex children via reshapeComplex.
 *
 * Refs collapsed to strings (Ref<T>/SimpleRef) — will be generated
 * by --collapse-refs after impl of issue #30
 */
function reshapeComplex(name: string, obj: Obj): Obj {
  switch (name) {
    case 'keyList':
      return keyListToXmlShape(obj, reshapeComplex);
    case 'privateCodes':
      return privateCodesToXmlShape(obj, reshapeComplex);
    case 'TextType':
      return textTypeToXmlShape(obj, reshapeComplex);
    case 'PrivateCode':
      return privateCodeToXmlShape(obj, reshapeComplex);
    case 'PassengerCapacityStructure':
      return passengerCapacityStructureToXmlShape(obj, reshapeComplex);
    case 'KeyValueStructure':
      return keyValueStructureToXmlShape(obj, reshapeComplex);
    case 'VehicleManoeuvringRequirement':
      return vehicleManoeuvringRequirementToXmlShape(obj, reshapeComplex);
    default:
      return obj;
  }
}

export function vehicleTypeToXmlShape(obj: Obj): Obj {
  return {
    ...attr(obj, 'id'),
    ...child(obj, 'keyList', 'keyList', reshapeComplex),
    ...child(obj, 'privateCodes', 'privateCodes', reshapeComplex),
    ...refAttr(obj, 'BrandingRef'),
    ...attr(obj, 'responsibilitySetRef'),
    ...mapArr(obj, 'Name', 'TextType', reshapeComplex),
    ...mapArr(obj, 'ShortName', 'TextType', reshapeComplex),
    ...mapArr(obj, 'Description', 'TextType', reshapeComplex),
    ...child(obj, 'PrivateCode', 'PrivateCode', reshapeComplex),
    ...elem(obj, 'EuroClass'),
    ...elem(obj, 'ReversingDirection'),
    ...elem(obj, 'SelfPropelled'),
    ...elem(obj, 'PropulsionTypes'),
    ...elem(obj, 'PropulsionType'),
    ...elem(obj, 'FuelTypes'),
    ...elem(obj, 'MaximumRange'),
    ...elem(obj, 'MaximumVelocity'),
    ...elem(obj, 'TransportMode'),
    ...child(obj, 'PassengerCapacity', 'PassengerCapacityStructure', reshapeComplex),
    ...refAttr(obj, 'DeckPlanRef'),
    ...elem(obj, 'LowFloor'),
    ...elem(obj, 'HasLiftOrRamp'),
    ...elem(obj, 'HasHoist'),
    ...elem(obj, 'HoistOperatingRadius'),
    ...elem(obj, 'BoardingHeight'),
    ...elem(obj, 'GapToPlatform'),
    ...elem(obj, 'Length'),
    ...elem(obj, 'Width'),
    ...elem(obj, 'Height'),
    ...elem(obj, 'Weight'),
    ...elem(obj, 'FirstAxleHeight'),
    ...elem(obj, 'Monitored'),
    ...refAttr(obj, 'IncludedIn'),
    ...refAttr(obj, 'ClassifiedAsRef'),
    ...elem(obj, 'facilities'),
    ...elem(obj, 'canCarry'),
    ...child(obj, 'canManoeuvre', 'VehicleManoeuvringRequirement', reshapeComplex),
    ...elem(obj, 'satisfiesFacilityRequirements'),
  };
}

function keyListToXmlShape(obj: Obj, reshapeComplex: Reshape): Obj {
  return {
    ...mapArr(obj, 'KeyValue', 'KeyValueStructure', reshapeComplex),
  };
}

function privateCodesToXmlShape(obj: Obj, reshapeComplex: Reshape): Obj {
  return {
    ...mapArr(obj, 'PrivateCode', 'PrivateCode', reshapeComplex),
  };
}

// eslint-disable-next-line @typescript-eslint/no-unused-vars
function textTypeToXmlShape(obj: Obj, _reshapeComplex: Reshape): Obj {
  return {
    ...text(obj),
    ...attr(obj, 'lang'),
    ...attr(obj, 'textIdType'),
  };
}

// eslint-disable-next-line @typescript-eslint/no-unused-vars
function privateCodeToXmlShape(obj: Obj, _reshapeComplex: Reshape): Obj {
  return {
    ...text(obj),
    ...attr(obj, 'type'),
  };
}

// eslint-disable-next-line @typescript-eslint/no-unused-vars
function passengerCapacityStructureToXmlShape(obj: Obj, _reshapeComplex: Reshape): Obj {
  return {
    ...elem(obj, 'FareClass'),
    ...elem(obj, 'TotalCapacity'),
    ...elem(obj, 'SeatingCapacity'),
    ...elem(obj, 'StandingCapacity'),
    ...elem(obj, 'SpecialPlaceCapacity'),
    ...elem(obj, 'PushchairCapacity'),
    ...elem(obj, 'WheelchairPlaceCapacity'),
    ...elem(obj, 'PramPlaceCapacity'),
    ...elem(obj, 'BicycleRackCapacity'),
  };
}

// eslint-disable-next-line @typescript-eslint/no-unused-vars
function keyValueStructureToXmlShape(obj: Obj, _reshapeComplex: Reshape): Obj {
  return {
    ...elem(obj, 'Key'),
    ...elem(obj, 'Value'),
    ...attr(obj, 'typeOfKey'),
  };
}

